import re

# Упрощённый классификатор эмоций по ключевым словам
# В будущем можно заменить на модель NLP

EMOTION_KEYWORDS = {
    "радость": ["ура", "класс", "супер", "отлично", "счастлив", "весело", "рад"],
    "грусть": ["печально", "грустно", "уныло", "жаль", "тоска", "расстроен"],
    "злость": ["бесит", "раздражает", "ненавижу", "злость", "чёрт"],
    "удивление": ["ничего себе", "невероятно", "правда?", "в смысле", "ого"],
    "страх": ["боюсь", "страшно", "опасно", "ужас", "паника"],
    "спокойствие": ["нормально", "спокойно", "ладно", "окей", "всё хорошо"],
}

def classify_emotion(text: str) -> str:
    text = text.lower()
    for emotion, keywords in EMOTION_KEYWORDS.items():
        for word in keywords:
            if re.search(rf"\b{re.escape(word)}\b", text):
                return emotion
    return "нейтрально"
